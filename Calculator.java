class Calculator
{
public static void main(String[] args)
{
Calculator calc = new Calculator();
calc.add();
calc.sub();
calc.multi();
calc.divi();
}
void add()
{
System.out.println(10+20);
}
void sub()
{
System.out.println(10-20);
}
void multi()
{
System.out.println(10*20);
}
void divi()
{
System.out.println(10/20);
}
}
